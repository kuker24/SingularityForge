#!/usr/bin/env node
import { readFileSync, writeFileSync, existsSync } from 'node:fs';
import path from 'node:path';
import os from 'node:os';

const root = process.cwd();

// Profile mapping definition
const PROFILE_MAP = {
  minimal: {
    rules: ['rules/token-discipline.md'],
    skills: ['skills/token-router/SKILL.md']
  },
  coding: {
    rules: ['rules/token-discipline.md', 'rules/engineering.md', 'rules/debugging.md', 'rules/testing.md'],
    skills: ['skills/token-router/SKILL.md', 'skills/verify-before-done/SKILL.md']
  },
  security: {
    rules: ['rules/token-discipline.md', 'rules/security.md'],
    skills: ['skills/token-router/SKILL.md', 'skills/security-review/SKILL.md']
  },
  'repo-review': {
    rules: ['rules/token-discipline.md', 'rules/engineering.md'],
    skills: ['skills/token-router/SKILL.md', 'skills/repo-intake/SKILL.md', 'skills/architecture-review/SKILL.md']
  },
  release: {
    rules: ['rules/token-discipline.md', 'rules/release.md'],
    skills: ['skills/token-router/SKILL.md', 'skills/release-check/SKILL.md']
  },
  max: {
    rules: [
      'rules/token-discipline.md',
      'rules/engineering.md',
      'rules/debugging.md',
      'rules/testing.md',
      'rules/security.md',
      'rules/release.md'
    ],
    skills: [
      'skills/token-router/SKILL.md',
      'skills/verify-before-done/SKILL.md',
      'skills/security-review/SKILL.md',
      'skills/repo-intake/SKILL.md',
      'skills/architecture-review/SKILL.md',
      'skills/release-check/SKILL.md',
      'skills/fable-mode/SKILL.md'
    ]
  }
};

// Determine target configuration directory
let targetDir = path.join(os.homedir(), '.claude');
let isLocal = false;

// If local project .claude/settings.json exists, optimize local directory instead
if (existsSync(path.join(root, '.claude/settings.json'))) {
  targetDir = path.join(root, '.claude');
  isLocal = true;
}

const settingsPath = path.join(targetDir, 'settings.json');
if (!existsSync(settingsPath)) {
  console.error(`Error: settings.json not found at ${settingsPath}`);
  process.exit(1);
}

// Read profile setting
let profile = 'minimal';
try {
  const settings = JSON.parse(readFileSync(settingsPath, 'utf8'));
  if (settings.singularityForge && settings.singularityForge.profile) {
    profile = settings.singularityForge.profile;
  }
} catch (e) {
  console.warn(`Warning: Failed to parse settings.json. Defaulting to 'minimal' profile.`);
}

console.log(`Optimizing Token Cache for directory: ${targetDir}`);
console.log(`Active Profile: ${profile} (Scope: ${isLocal ? 'Local Workspace' : 'Global Runtime'})`);

const config = PROFILE_MAP[profile] || PROFILE_MAP.minimal;

// Base global memory (CLAUDE.md header template)
let compiledContent = `# SingularityForge Memory Cache [Profile: ${profile}]

This is an optimized memory cache generated automatically by SingularityForge.
Do not edit this file manually. Change settings.json or template rules instead.

---

`;

let rawWordCount = 0;
let optimizedWordCount = 0;

// Read and compress helper
function optimizeMarkdown(filePath, baseName) {
  if (!existsSync(filePath)) {
    console.warn(`Warning: Source file not found: ${filePath}`);
    return '';
  }
  const content = readFileSync(filePath, 'utf8');
  
  // Word count analysis
  const words = content.split(/\s+/).filter(Boolean).length;
  rawWordCount += words;

  // Optimizations
  let optimized = content
    // Remove Markdown comments <!-- ... -->
    .replace(/<!--[\s\S]*?-->/g, '')
    // Remove multiple blank lines
    .replace(/\n\s*\n/g, '\n')
    // Remove leading/trailing spaces on each line
    .split('\n')
    .map(line => line.trim())
    .filter(Boolean)
    .join('\n');

  const optWords = optimized.split(/\s+/).filter(Boolean).length;
  optimizedWordCount += optWords;

  return `\n## [Source: ${baseName}]\n${optimized}\n`;
}

// Compile rules
for (const ruleRel of config.rules) {
  let filePath = path.join(targetDir, ruleRel);
  if (!existsSync(filePath)) {
    filePath = path.join(root, 'packages', ruleRel);
  }
  if (!existsSync(filePath)) {
    filePath = path.join(os.homedir(), '.claude', ruleRel);
  }
  compiledContent += optimizeMarkdown(filePath, ruleRel);
}

// Compile skills
for (const skillRel of config.skills) {
  let filePath = path.join(targetDir, skillRel);
  if (!existsSync(filePath)) {
    filePath = path.join(root, 'packages', skillRel);
  }
  if (!existsSync(filePath)) {
    filePath = path.join(os.homedir(), '.claude', skillRel);
  }
  compiledContent += optimizeMarkdown(filePath, skillRel);
}

// Write the optimized CLAUDE.md
const outputClaudePath = path.join(targetDir, 'CLAUDE.md');
writeFileSync(outputClaudePath, compiledContent, 'utf8');

console.log(`\nToken Optimization Report:`);
console.log(`- Output File: ${outputClaudePath}`);
console.log(`- Original Token Weight (Word Count): ${rawWordCount} words`);
console.log(`- Optimized Token Weight (Word Count): ${optimizedWordCount} words`);
const savings = rawWordCount > 0 ? ((rawWordCount - optimizedWordCount) / rawWordCount * 100).toFixed(1) : 0;
console.log(`- Token Budget Savings: ${savings}% reduced context footprint`);
console.log(`Optimization Completed Successfully.`);
