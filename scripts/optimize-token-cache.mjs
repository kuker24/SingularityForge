#!/usr/bin/env node
import { readFileSync, writeFileSync, existsSync } from 'node:fs';
import path from 'node:path';
import os from 'node:os';

const root = process.cwd();

// Profile mapping definition
const PROFILE_MAP = {
  minimal: {
    rules: ['rules/token-discipline.md'],
    skills: ['skills/token-router/SKILL.md']
  },
  coding: {
    rules: ['rules/token-discipline.md', 'rules/engineering.md', 'rules/debugging.md', 'rules/testing.md'],
    skills: ['skills/token-router/SKILL.md', 'skills/verify-before-done/SKILL.md']
  },
  security: {
    rules: ['rules/token-discipline.md', 'rules/security.md'],
    skills: ['skills/token-router/SKILL.md', 'skills/security-review/SKILL.md']
  },
  'repo-review': {
    rules: ['rules/token-discipline.md', 'rules/engineering.md'],
    skills: ['skills/token-router/SKILL.md', 'skills/repo-intake/SKILL.md', 'skills/architecture-review/SKILL.md']
  },
  release: {
    rules: ['rules/token-discipline.md', 'rules/release.md'],
    skills: ['skills/token-router/SKILL.md', 'skills/release-check/SKILL.md']
  },
  max: {
    rules: [
      'rules/token-discipline.md',
      'rules/engineering.md',
      'rules/debugging.md',
      'rules/testing.md',
      'rules/security.md',
      'rules/release.md'
    ],
    skills: [
      'skills/token-router/SKILL.md',
      'skills/verify-before-done/SKILL.md',
      'skills/security-review/SKILL.md',
      'skills/repo-intake/SKILL.md',
      'skills/architecture-review/SKILL.md',
      'skills/release-check/SKILL.md',
      'skills/fable-mode/SKILL.md'
    ]
  }
};

// Parse command line arguments
let targetDirArg = null;
let forcedProfile = null;

for (let i = 2; i < process.argv.length; i++) {
  const arg = process.argv[i];
  if (arg === '--profile' || arg === '-p') {
    forcedProfile = process.argv[i + 1];
    i++;
  } else {
    targetDirArg = arg;
  }
}

// Validate forced profile if set
if (forcedProfile && forcedProfile !== 'all' && !PROFILE_MAP[forcedProfile]) {
  console.error(`Error: Invalid profile '${forcedProfile}'. Supported profiles: ${Object.keys(PROFILE_MAP).join(', ')}, all`);
  process.exit(1);
}

// Determine target configuration directory
let targetDir = targetDirArg;
let isLocal = false;

if (targetDir) {
  targetDir = path.resolve(targetDir);
  const globalClaudePath = path.resolve(path.join(os.homedir(), '.claude'));
  if (targetDir !== globalClaudePath) {
    isLocal = true;
  }
} else {
  if (existsSync(path.join(root, '.claude/settings.json'))) {
    targetDir = path.join(root, '.claude');
    isLocal = true;
  } else {
    targetDir = path.join(os.homedir(), '.claude');
  }
}

const settingsPath = path.join(targetDir, 'settings.json');
let defaultProfile = 'minimal';

if (existsSync(settingsPath)) {
  try {
    const settings = JSON.parse(readFileSync(settingsPath, 'utf8'));
    if (settings.singularityForge && settings.singularityForge.profile) {
      defaultProfile = settings.singularityForge.profile;
    }
  } catch (e) {
    // Defaulting silently or with simple warning to avoid CI crashes
  }
}

// Determine profile(s) to optimize
const profilesToRun = [];
if (forcedProfile === 'all') {
  profilesToRun.push(...Object.keys(PROFILE_MAP));
} else if (forcedProfile) {
  profilesToRun.push(forcedProfile);
} else {
  profilesToRun.push(defaultProfile);
}

// Helper to count words
function countWords(text) {
  return text.split(/\s+/).filter(Boolean).length;
}

// Helper to resolve source path
function resolveSourcePath(fileRel) {
  let filePath = path.join(root, 'packages', fileRel);
  if (!existsSync(filePath)) {
    filePath = path.join(targetDir, fileRel);
  }
  if (!existsSync(filePath)) {
    filePath = path.join(os.homedir(), '.claude', fileRel);
  }
  return filePath;
}

// Optimization routine for a single profile
function optimizeProfile(profileName) {
  const config = PROFILE_MAP[profileName] || PROFILE_MAP.minimal;
  
  let compiledContent = `# SingularityForge Memory Cache [Profile: ${profileName}]

This is an optimized memory cache generated automatically by SingularityForge.
Do not edit this file manually. Change settings.json or template rules instead.

---

`;

  let rawWordCount = 0;
  let optimizedWordCount = 0;

  function optimizeMarkdown(filePath, baseName) {
    if (!existsSync(filePath)) {
      return '';
    }
    const content = readFileSync(filePath, 'utf8');
    rawWordCount += countWords(content);

    // Compress content (remove markdown comments and extra blank spaces)
    let optimized = content
      .replace(/<!--[\s\S]*?-->/g, '')
      .replace(/\n\s*\n/g, '\n')
      .split('\n')
      .map(line => line.trim())
      .filter(Boolean)
      .join('\n');

    optimizedWordCount += countWords(optimized);
    return `\n## [Source: ${baseName}]\n${optimized}\n`;
  }

  // Include base CLAUDE.md template if available
  const baseClaudePath = resolveSourcePath('global-memory/CLAUDE.md');
  compiledContent += optimizeMarkdown(baseClaudePath, 'global-memory/CLAUDE.md');

  // Compile rules
  for (const ruleRel of config.rules) {
    const filePath = resolveSourcePath(ruleRel);
    compiledContent += optimizeMarkdown(filePath, ruleRel);
  }

  // Compile skills
  for (const skillRel of config.skills) {
    const filePath = resolveSourcePath(skillRel);
    compiledContent += optimizeMarkdown(filePath, skillRel);
  }

  // Save cache output
  // For 'all' profile runs or forced custom profiles, we save to a specific file to avoid overwriting default user memory,
  // but if it is the active/forced single profile, we save to 'CLAUDE.md'
  const isDefaultOrSingleForced = !forcedProfile || forcedProfile === profileName;
  const fileName = isDefaultOrSingleForced ? 'CLAUDE.md' : `CLAUDE.${profileName}.md`;
  const outputClaudePath = path.join(targetDir, fileName);

  writeFileSync(outputClaudePath, compiledContent, 'utf8');

  const estimatedTokens = Math.ceil(optimizedWordCount * 1.3);

  console.log(`Optimizing Token Cache: ${profileName.toUpperCase()}`);
  console.log(`  - Target Directory: ${targetDir}`);
  console.log(`  - Active Profile: ${profileName}`);
  console.log(`  - Selected Rules: ${config.rules.join(', ')}`);
  console.log(`  - Selected Skills: ${config.skills.join(', ')}`);
  console.log(`  - Original Words: ${rawWordCount}`);
  console.log(`  - Optimized Words: ${optimizedWordCount}`);
  console.log(`  - Estimated Tokens: ${estimatedTokens}`);
  console.log(`  - Cache Path: ${outputClaudePath}`);
  console.log(`-----------------------------------------------`);
}

console.log(`Optimizing Token Cache for directory: ${targetDir}`);
for (const p of profilesToRun) {
  optimizeProfile(p);
}
console.log(`Optimization Completed Successfully.`);
